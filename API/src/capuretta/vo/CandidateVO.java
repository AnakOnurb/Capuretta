package capuretta.vo;

public class CandidateVO 
{
	private int id;
	private String nome;
	private String cpf;
	private String endereco;
	private String telefone;
	private String descricao;
	
	public int getId() { return this.id; }
	public void setId(int id) { this.id = id; }
	
	public String getNome() { return this.nome; }
	public void setNome(int petId) { this.nome = nome; }
	
	public String getCpf() { return this.cpf; }
	public void setCpf(int petId) { this.cpf = cpf; }
	
	public String getEndereco() { return this.endereco; }
	public void setEndereco(int petId) { this.endereco = endereco; }
	
	public String getTelefone() { return this.telefone; }
	public void setTelefone(int petId) { this.telefone = telefone; }
	
	public String getDescricao() { return this.descricao; }
	public void setDescricao(int petId) { this.descricao = descricao; }
}
